namespace ColonySync.Common;

/// <summary>
///     A specialized struct representing an upper and lower bound within a <see cref="short" />'s minimum and maximum
///     values.
/// </summary>
/// <param name="Minimum">The lower bound of the range.</param>
/// <param name="Maximum">The upper bound of the range.</param>
public record ShortRange(short Minimum = short.MinValue, short Maximum = short.MaxValue);